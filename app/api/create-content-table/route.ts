import { sql } from '@vercel/postgres';
import { NextResponse } from 'next/server';
 
export async function GET(request: Request) {
  try {
    const result =
      await sql`CREATE TABLE Content ( Title varchar(255), Subtitle varchar(255), Description varchar(255), Year varchar(255), Role varchar(255), Page varchar(255), Image varchar(255), Type varchar(255), Status varchar(255) );`;
    return NextResponse.json({ result }, { status: 200 });
  } catch (error) {
    return NextResponse.json({ error }, { status: 500 });
  }
}